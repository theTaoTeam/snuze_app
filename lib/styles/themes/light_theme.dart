import 'package:flutter/material.dart';

ThemeData buildLightTheme() {
  return ThemeData(
    backgroundColor: Colors.white,
    brightness: Brightness.light,
    primaryColor: Colors.white,
    accentColor: Colors.red,
  );
}

ThemeData buildDarkTheme() {
  return ThemeData(
    backgroundColor: Colors.white,
    brightness: Brightness.light,
    primaryColor: Colors.white,
    accentColor: Colors.red,
  );
}
